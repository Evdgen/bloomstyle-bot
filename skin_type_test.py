user_skin_test_states = {}

def start_skin_type_test(user_id):
    """Начать тест на определение типа кожи"""
    user_skin_test_states[user_id] = {
        "answers": [],
        "current_q": 0
    }
    return SKIN_TYPE_QUESTIONS[0]["question"], list(SKIN_TYPE_QUESTIONS[0]["options"].keys())

def process_skin_type_answer(user_id, answer):
    """Обработать ответ и вернуть следующий вопрос или результат"""
    if user_id not in user_skin_test_states:
        return None, None
    
    state = user_skin_test_states[user_id]
    q_index = state["current_q"]
    
    # Сохраняем ответ
    current_q = SKIN_TYPE_QUESTIONS[q_index]
    skin_value = None
    for option, value in current_q["options"].items():
        if option == answer:
            skin_value = value
            break
    
    if skin_value:
        state["answers"].append(skin_value)
    
    # Переходим к следующему вопросу
    state["current_q"] += 1
    
    if state["current_q"] >= len(SKIN_TYPE_QUESTIONS):
        # Тест завершен - подсчитываем результат
        result = calculate_skin_type(state["answers"])
        del user_skin_test_states[user_id]
        return result, None
    else:
        # Следующий вопрос
        next_q = SKIN_TYPE_QUESTIONS[state["current_q"]]
        return next_q["question"], list(next_q["options"].keys())

def calculate_skin_type(answers):
    """Подсчет результатов теста типа кожи"""
    scores = {"dry": 0, "oily": 0, "combination": 0, "normal": 0, "sensitive": 0}
    
    for answer in answers:
        if answer in scores:
            scores[answer] += 1
    
    # Определяем победителя
    winner = max(scores, key=scores.get)
    return {
        "key": winner,
        "result": SKIN_TYPE_RESULTS[winner]
    }

def get_skin_type_description(skin_key):
    """Получить полное описание типа кожи"""
    st = SKIN_TYPE_RESULTS.get(skin_key, SKIN_TYPE_RESULTS["normal"])
    
    text = f"{st['name']}\n\n"
    text += f"📝 *ОПИСАНИЕ:* {st['description']}\n\n"
    text += f"🔍 *ХАРАКТЕРИСТИКИ:*\n"
    for char in st['characteristics']:
        text += f"{char}\n"
    text += f"\n💆‍♀️ *РЕКОМЕНДУЕМАЯ РУТИНА:*\n{st['routine']}\n\n"
    text += f"✅ *ПОЛЕЗНЫЕ ИНГРЕДИЕНТЫ:*\n"
    for ing in st['ingredients']:
        text += f"• {ing}\n"
    text += f"\n❌ *ИЗБЕГАТЬ:*\n"
